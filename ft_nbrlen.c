#include "libft.h"

int	ft_nbrlen(long n)
{
	size_t	x;

	x = 1;
	if (n < 0)
	{
		n = n * -1;
			x++;
	}
	while (n >= 10)
	{
		n = n / 10;
		x++;
	}
	return (x);
}
